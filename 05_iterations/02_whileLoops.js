// while loops
let index = 0
while (index <= 10) {
    // console.log(`Value of index is ${index}`);
    index = index + 2;
}
//Array while loops
let myArray = ["Flash", "Bat-Man", "Super-Man"]

let arr = 0
while (arr < myArray.length) {
    console.log(`Value is ${myArray[arr]}`);
    arr = arr + 1;
}

// do-while loops
let score = 11

do {
    console.log(`Score is ${score}`);
    score++
} while (score <= 10);